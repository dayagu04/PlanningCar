"""Compare navigation algorithms across terrains — generates top-level figures.

Refreshed for iter30: pulls aggregated metrics from metrics_v2 + per-seed traces
from data/experiments/iter{CURRENT_ITER}/. Includes all 3 controllers (adaptive,
baseline PD, A*).

Outputs:
  results/figures/metrics_comparison.png
  results/figures/trajectory_comparison.png
  results/figures/speed_comparison.png
  results/figures/metrics_table.csv

The chapter-6 thesis figures are now generated by generate_chapter6_figures.py
to keep top-level vs thesis figures decoupled.
"""

import os
import sys

import numpy as np
import pandas as pd
import matplotlib.pyplot as plt

PROJECT_ROOT = os.path.abspath(os.path.join(os.path.dirname(__file__), "..", ".."))
sys.path.insert(0, PROJECT_ROOT)
sys.path.insert(0, os.path.join(PROJECT_ROOT, "scripts", "figures"))
from _iter_data import (CURRENT_ITER, DEFAULT_SEED, TERRAIN_KEYS,
                        CONTROLLERS, CONTROLLER_LABEL, CONTROLLER_COLOR,
                        load_metrics, trace_path, metric_value)

FIGURES_DIR = os.path.join(PROJECT_ROOT, "results", "figures")
os.makedirs(FIGURES_DIR, exist_ok=True)

plt.rcParams["font.sans-serif"] = ["SimHei", "Microsoft YaHei", "DejaVu Sans"]
plt.rcParams["axes.unicode_minus"] = False


def load_trace(terrain, ctrl, seed=DEFAULT_SEED):
    path = trace_path(terrain, ctrl, seed)
    if not os.path.exists(path):
        return None
    df = pd.read_csv(path)
    dt = df["time_s"].diff().fillna(0.032)
    df["speed_actual"] = np.sqrt(df["x"].diff() ** 2 + df["y"].diff() ** 2) / dt
    df["speed_actual"] = df["speed_actual"].clip(0, 15).fillna(0)
    return df


def plot_metrics_comparison(metrics, save_path):
    """4 panels: success_rate, completion, path_efficiency, time_to_goal."""
    panels = [
        ("success_rate", "Success Rate", 1.0),
        ("completion_fraction", "Completion Fraction", 1.0),
        ("path_efficiency", "Path Efficiency", 1.0),
        ("time_to_goal", "Time to Goal (s)", None),
    ]

    fig, axes = plt.subplots(2, 2, figsize=(13, 9))
    axes = axes.flatten()
    x = np.arange(len(TERRAIN_KEYS))
    width = 0.26

    for idx, (key, label, ymax) in enumerate(panels):
        ax = axes[idx]
        peak = 0.0
        for i, ctrl in enumerate(CONTROLLERS):
            values = []
            for terrain in TERRAIN_KEYS:
                v = metric_value(metrics, terrain, ctrl, key)
                values.append(v if v is not None else 0.0)
            peak = max(peak, max(values) if values else 0.0)
            offset = (i - 1) * width
            ax.bar(x + offset, values, width, label=CONTROLLER_LABEL[ctrl],
                   color=CONTROLLER_COLOR[ctrl], alpha=0.88,
                   edgecolor="black", linewidth=0.5)
            for j, v in enumerate(values):
                if v > 0.01:
                    ax.text(x[j] + offset, v + (peak * 0.02 + 0.005),
                            f"{v:.2f}", ha="center", va="bottom", fontsize=7)
        ax.set_xticks(x)
        ax.set_xticklabels([t.title() for t in TERRAIN_KEYS])
        ax.set_ylabel(label)
        ax.set_title(label)
        ax.legend(fontsize=8, loc="upper right")
        ax.grid(True, alpha=0.3, axis="y")
        if ymax is not None:
            ax.set_ylim(0, ymax * 1.15)

    plt.suptitle(f"Algorithm Comparison Across Terrains (iter{CURRENT_ITER}, "
                 f"5 seeds aggregated)", fontsize=13, fontweight="bold")
    plt.tight_layout(rect=[0, 0, 1, 0.97])
    plt.savefig(save_path, dpi=150, bbox_inches="tight")
    plt.close()
    print(f"Saved: {save_path}")


def plot_trajectory_comparison(save_path):
    """3x4 grid: 3 controllers, 4 terrains, single representative seed."""
    fig, axes = plt.subplots(3, 4, figsize=(16, 11))
    terrain_colors = {"flat": "#4CAF50", "slope": "#FF9800",
                      "rough": "#F44336", "transition": "#9C27B0"}

    for col, terrain in enumerate(TERRAIN_KEYS):
        for row, ctrl in enumerate(CONTROLLERS):
            ax = axes[row, col]
            df = load_trace(terrain, ctrl)
            if df is not None:
                for t_type, color in terrain_colors.items():
                    mask = df["terrain"] == t_type
                    if mask.any():
                        ax.scatter(df.loc[mask, "x"], df.loc[mask, "y"],
                                   c=color, s=3, alpha=0.6)
                ax.plot(df["x"].iloc[0], df["y"].iloc[0], "go", markersize=8,
                        markeredgecolor="black", markeredgewidth=0.5)
                ax.plot(df["x"].iloc[-1], df["y"].iloc[-1], "r^", markersize=8,
                        markeredgecolor="black", markeredgewidth=0.5)
            ax.set_xlim(-10, 10)
            ax.set_ylim(-10, 10)
            ax.set_aspect("equal")
            ax.grid(True, alpha=0.3)
            if row == 0:
                ax.set_title(terrain.title(), fontsize=11, fontweight="bold")
            if col == 0:
                ax.set_ylabel(CONTROLLER_LABEL[ctrl], fontsize=10,
                              fontweight="bold")

    plt.suptitle(f"Trajectory Comparison (iter{CURRENT_ITER}, "
                 f"seed={DEFAULT_SEED})", fontsize=13, fontweight="bold")
    plt.tight_layout(rect=[0, 0, 1, 0.97])
    plt.savefig(save_path, dpi=150, bbox_inches="tight")
    plt.close()
    print(f"Saved: {save_path}")


def plot_speed_comparison(save_path):
    """2x2 panels: speed profile per terrain, all 3 controllers overlaid."""
    fig, axes = plt.subplots(2, 2, figsize=(13, 8))
    axes = axes.flatten()

    for idx, terrain in enumerate(TERRAIN_KEYS):
        ax = axes[idx]
        for ctrl in CONTROLLERS:
            df = load_trace(terrain, ctrl)
            if df is not None and len(df) > 5:
                speed = df["speed_actual"].rolling(5, min_periods=1).mean()
                ax.plot(df["time_s"], speed, linewidth=1.3,
                        color=CONTROLLER_COLOR[ctrl],
                        label=CONTROLLER_LABEL[ctrl], alpha=0.85)
        ax.set_xlabel("Time (s)")
        ax.set_ylabel("Speed (m/s)")
        ax.set_title(terrain.title())
        ax.legend(fontsize=8)
        ax.grid(True, alpha=0.3)
        ax.set_ylim(bottom=0)

    plt.suptitle(f"Speed Profile Comparison (iter{CURRENT_ITER}, "
                 f"seed={DEFAULT_SEED})", fontsize=13, fontweight="bold")
    plt.tight_layout(rect=[0, 0, 1, 0.97])
    plt.savefig(save_path, dpi=150, bbox_inches="tight")
    plt.close()
    print(f"Saved: {save_path}")


def write_metrics_table(metrics, save_path):
    rows = []
    keys = ["success_rate", "completion_fraction", "path_efficiency",
            "time_to_goal", "actual_path_length", "classification_accuracy",
            "cross_track_mean", "replan_count"]
    for terrain in TERRAIN_KEYS:
        for ctrl in CONTROLLERS:
            row = {"Terrain": terrain.title(),
                   "Algorithm": CONTROLLER_LABEL[ctrl]}
            for k in keys:
                v = metric_value(metrics, terrain, ctrl, k)
                row[k] = v
            row["n_seeds"] = metrics["results"][terrain][ctrl]["success_rate"]["n"]
            rows.append(row)
    pd.DataFrame(rows).to_csv(save_path, index=False, encoding="utf-8-sig")
    print(f"Saved: {save_path}")


def main():
    print(f"Loading iter{CURRENT_ITER} metrics + traces (seed {DEFAULT_SEED})...")
    metrics = load_metrics()

    plot_metrics_comparison(metrics,
                            os.path.join(FIGURES_DIR, "metrics_comparison.png"))
    plot_trajectory_comparison(os.path.join(FIGURES_DIR,
                                            "trajectory_comparison.png"))
    plot_speed_comparison(os.path.join(FIGURES_DIR, "speed_comparison.png"))
    write_metrics_table(metrics, os.path.join(FIGURES_DIR, "metrics_table.csv"))


if __name__ == "__main__":
    main()
